public class Result {

	private String fileName;
	private String flowCell;
	private String project;
	private String subProject;
	private String method;
	private String id;
	private int code;

	private int A_freq;
	private int C_freq;
	private int G_freq;
	private int T_freq;

	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getFlowCell() {
		return flowCell;
	}
	public void setFlowCell(String flowCell) {
		this.flowCell = flowCell;
	}
	public String getProject() {
		return project;
	}
	public void setProject(String project) {
		this.project = project;
	}
	public String getSubProject() {
		return subProject;
	}
	public void setSubProject(String subProject) {
		this.subProject = subProject;
	}
	public String getMethod() {
		return method;
	}
	public void setMethod(String method) {
		this.method = method;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public int getA_freq() {
		return A_freq;
	}
	public void setA_freq(int a_freq) {
		A_freq = a_freq;
	}
	public int getC_freq() {
		return C_freq;
	}
	public void setC_freq(int c_freq) {
		C_freq = c_freq;
	}
	public int getG_freq() {
		return G_freq;
	}
	public void setG_freq(int g_freq) {
		G_freq = g_freq;
	}
	public int getT_freq() {
		return T_freq;
	}
	public void setT_freq(int t_freq) {
		T_freq = t_freq;
	}

}
